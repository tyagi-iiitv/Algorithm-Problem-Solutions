    public static ArrayList<String> deBruijnFromKmers( String[] kmers ) {
        HashSet<String> temp = new HashSet<>();
        for(int i = 0; i < kmers.length; ++i) {
            temp.add(kmers[i]);
            temp.add(reverseComplement(kmers[i]));
        }
        String[] kmersUnion = new String[temp.size()];
        int add = 0;
        Iterator itr = temp.iterator();
        while(itr.hasNext()) {
            kmersUnion[add++] = (String)itr.next();
        }
        String[] paths = new String[kmersUnion.length];
        for(int i = 0; i < paths.length; ++i) {
            String curr = kmersUnion[i];
            paths[i] = (curr.substring(0,curr.length()-1) + "." + curr.substring(1,curr.length()));
        }
        Arrays.sort(paths);
        
        ArrayList<String> out = new ArrayList<>();
        String start = "";
        ArrayList<String> ends = new ArrayList<String>();
        for(int i = 0; i < paths.length; ++i) {
            String[] edge = paths[i].split("\\.");
            if(start.equals(edge[0])) {
                ends.add(edge[1]);
            }
            else {
                if(ends.size() > 0) {
                    //String add = start + " -> ";
                    for(int j = 0; j < ends.size(); ++j) {
                        out.add("(" + start + ", " + ends.get(j) + ")");
                    }
                    //out.add(add.substring(0,add.length()-1));*/
                }
                
                start = edge[0];
                ends = new ArrayList<>();
                ends.add(edge[1]);
            }
        }
        for(int j = 0; j < ends.size(); ++j) {
            out.add("(" + start + ", " + ends.get(j) + ")");
        }
        return out;
    }
    
    public static String reverseComplement( String dna ) {
        String out = "";
        for(int i = dna.length() - 1; i >= 0; --i) {
            char curr = dna.charAt(i);
            if(curr == 'A')
                out += 'T';
            else if(curr == 'T')
                out += 'A';
            else if(curr == 'C')
                out += 'G';
            else if(curr == 'G')
                out += 'C';
            else {
                System.out.println("ERROR: Input is not a DNA Sequence.");
                System.exit(-1);
            }
        }
        return out;
    }
