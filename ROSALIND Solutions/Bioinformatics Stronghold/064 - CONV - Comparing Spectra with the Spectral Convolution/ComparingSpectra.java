  public static String[] comparingSpectra( double[] s1, double[] s2 ) {
    double[] diff = new double[s1.length*s2.length];
    int pos = 0;
    for(double i : s1) {
      for(double j : s2) {
        diff[pos++] = i-j;
      }
    }
    int[] diffInt = new int[diff.length];
    for(int i = 0; i < diff.length; ++i) {
      diffInt[i] = (int)(diff[i]*100000 + 0.5);
    }
    HashMap<Integer,Integer> counts = new HashMap<>();
    for(int v : diffInt) {
      if(counts.containsKey(v)) {
        counts.put(v,counts.get(v)+1);
      }
      else {
        counts.put(v,1);
      }
    }
    int maxC = 0;
    int maxV = 0;
    for(int v : diffInt) {
      int count = counts.get(v);
      if(count > maxC) {
        maxC = count;
        maxV = v;
      }
    }
    String[] out = new String[2];
    out[0] = Integer.toString(maxC);
    String val = Double.toString((double)maxV / 100000.0);
    out[1] = val.substring(0,val.indexOf('.')+6);
    return out;
  }
